const HeaderBlock = ({menu = [], color, background}) => {

  const router = useRouter()

  const [state, setState] = React.useState({
    top: false,
  })

  const toggleDrawer = (anchor, open) => (event = {}) => {
    if (
      event.type === 'keydown' &&
      (event.key === 'Tab' || event.key === 'Shift')
    ) {
      return
    }

    setState({...state, [anchor]: open})
  }

  const handleClick = (e) => {
    e.preventDefault()
    router.push('/')
  }

  
  return (
    <A.Root>
      <A.Header
        container
        alignItems="center"
        direction="row"
        justifyContent="space-between"
        spacing={0}
        background={background}
      >
        <A.LogoBlock item xs={6} sm={2} md={4}>
          <A.Logo onClick={handleClick}/>
        </A.LogoBlock>

        <Grid item xs={6} sm={10} md={8}>
          {/* spacing one of [0,8,16,24,32,40]. */}

          <Hidden only={['md', 'lg', 'xl']}>
            <A.WrapperIcon>
              {['top'].map(anchor => (
                <React.Fragment key={anchor}>

                  <A.BlueButton style={{marginRight: `1em`}} align="center" variant="h5" component="h5" onClick={toggleDrawer(anchor, true)} >
                      <span style={{color: `#fff`}}>menu</span>
                  </A.BlueButton>


                  <A.SwipeableDrawer
                    anchor={anchor}
                    open={state[anchor]}
                    onClose={toggleDrawer(anchor, false)}
                    onOpen={toggleDrawer(anchor, true)}
                  >
                    <A.FullList
                      role="presentation"
                      onClick={toggleDrawer(anchor, false)}
                      onKeyDown={toggleDrawer(anchor, false)}
                    >
                      <List>
                        {menu.map(({title, url, type}, index) => (
                          <Link key={index} href={`${url}`} style={{color: `rgb(0,90,144)`}}>
                            {title}
                          </Link>
                        ))}
                      </List>
                    </A.FullList>
                    
                  </A.SwipeableDrawer>
                </React.Fragment>
              ))}
            </A.WrapperIcon>

          </Hidden>


          <Hidden only={['xs', 'sm']}>
            <A.MenuWrap
              container
              justifyContent="space-between"
              alignItems="center"
              spacing={0}
            >
              {menu.map(({title, url, type}, index) => (
                <Grid key={index} item>
                  {type === 'primary' ? (
                    <A.BlueButton align="center" variant="h5" component="h5" >
                        <A.Link href={`${url}`} style={{color: `#fff`}}><span style={{color: `#fff`}}>{title}</span></A.Link>
                    </A.BlueButton>
                  ) : url && type !== 'primary' ? (
                      <A.Link href={`${url}`}  style={{color: `rgb(0,90,144)`}}>{title}</A.Link>
                  ) : null}
                </Grid>
              ))}
            </A.MenuWrap>
          </Hidden>
        </Grid>
      </A.Header>
    </A.Root>
  );
}

export default HeaderBlock
